"""Deterministic org-wide diagram rendering from the compiled interface and dependency indices
(design D3/D4; dependency-indexing capability extends this to render dependency edges alongside
interface edges — architecture-diagrams spec, "Org diagram document shape").

Pure function, same shape as ``docs.render_interface_docs()``: compiled indices in, org diagram
markdown out. No LLM involvement — the content is mechanically derived from the compiled indices'
``owner``/``producer``/``consumer`` data, so it can never disagree with the index that produced it
and is exempt from doc-drift checking by construction.

One ``## {repo}`` section per repo with at least one *external* interface or dependency
(alphabetical), each with a small relationship graph (this repo as center node, one node per
related repo; interface edges dashed, dependency edges solid, and a dependency linked to an
interface via ``panopticon-dependency-of`` collapsed into one thick/double edge representing both —
architecture-diagrams spec, "Linked dependency and interface edges deduplicate") and a table of
that repo's external interfaces/dependencies. Repos whose every interface/dependency is
internal-only (design's exclusion rule: the union of an entry's owner/producer/consumer repos names
exactly one repo) get no section.
"""

import re
from pathlib import Path

from .config import DEFAULT_DIAGRAM_FORMAT

# Instance repo root, distinct from docs/{repo}/architecture.md (each child repo's own copy).
ORG_DIAGRAM_PATH = Path("docs") / "architecture.md"

_GENERATED_HEADER = (
    "<!-- Generated by panopticon.diagrams from the compiled interface and dependency indices.\n"
    "     Do not edit by hand: rebuilt automatically on every merge to a child repo's default branch. -->"
)

_MERMAID_ID_RE = re.compile(r"[^A-Za-z0-9_]")


def _mermaid_id(repo):
    return "repo_" + _MERMAID_ID_RE.sub("_", repo)


def repo_set(entry):
    """The union of repo names in one interface/dependency entry's owner/producer/consumer
    (design's internal-only exclusion rule input) — the same field shape serves both schemas."""
    repos = set()
    owner = entry.get("owner")
    if owner:
        repos.add(owner["repo"])
    for role in ("producer", "consumer"):
        repos.update(robj["repo"] for robj in entry[role])
    return repos


def _other_repo_role(entry, other_repo):
    """`other_repo`'s role(s) for this entry — owner, producer, and/or consumer."""
    roles = []
    owner = entry.get("owner")
    if owner and owner["repo"] == other_repo:
        roles.append("owner")
    if any(robj["repo"] == other_repo for robj in entry["producer"]):
        roles.append("producer")
    if any(robj["repo"] == other_repo for robj in entry["consumer"]):
        roles.append("consumer")
    return roles


def _rows_for_index(doc, key, type_field, kind, repo):
    """External rows for `repo` from one compiled index (interfaces or dependencies): one row per
    (entry, other-repo) pair, sorted by name then other repo (per-entry, not deduplicated)."""
    rows = []
    for name in sorted(doc.get(key, {})):
        for entry in doc[key][name]:
            repos = repo_set(entry)
            if len(repos) <= 1 or repo not in repos:
                continue
            produces = any(robj["repo"] == repo for robj in entry["producer"])
            consumes = any(robj["repo"] == repo for robj in entry["consumer"])
            direction = [d for d, flag in (("produces", produces), ("consumes", consumes)) if flag]
            if not direction:
                # repo owns this entry but neither produces nor consumes it itself.
                direction = ["owns"]
            for other in sorted(repos - {repo}):
                rows.append(
                    {
                        "kind": kind,
                        "name": name,
                        "type": entry[type_field],
                        "direction": "/".join(direction),
                        "produces": produces,
                        "consumes": consumes,
                        "other_repo": other,
                        "other_role": "/".join(_other_repo_role(entry, other)) or "none",
                        "links_to_interface": entry.get("links_to_interface"),
                    }
                )
    return rows


def _dedupe_linked_rows(rows):
    """Collapse a dependency row into its linked interface row when `links_to_interface` names an
    interface row relating the same two repos (architecture-diagrams spec, "Linked dependency and
    interface edges deduplicate") — never guessed, only when an explicit hint set the link."""
    interface_positions = {
        (r["name"], r["type"], r["other_repo"]): i
        for i, r in enumerate(rows)
        if r["kind"] == "interface"
    }
    consumed = set()
    merged_at = {}
    for i, row in enumerate(rows):
        if row["kind"] != "dependency" or not row.get("links_to_interface"):
            continue
        link = row["links_to_interface"]
        iface_idx = interface_positions.get((link["name"], link["type"], row["other_repo"]))
        if iface_idx is None or iface_idx in consumed:
            continue
        combined = dict(rows[iface_idx])
        combined["kind"] = "linked"
        combined["name"] = f"{rows[iface_idx]['name']} + {row['name']}"
        merged_at[i] = combined
        consumed.update((iface_idx, i))
    result = []
    for i, row in enumerate(rows):
        if i in merged_at:
            result.append(merged_at[i])
        elif i in consumed:
            continue
        else:
            result.append(row)
    return result


def relationships_for_repo(interfaces_compiled, repo, dependencies_compiled=None):
    """External interface and dependency rows for `repo`, combined into one list (deduplicated
    when a dependency is explicitly linked to an interface). Empty when every one of `repo`'s
    entries is internal-only. `dependencies_compiled` defaults to empty — interface-only callers
    are unaffected."""
    rows = _rows_for_index(interfaces_compiled, "interfaces", "type", "interface", repo)
    rows += _rows_for_index(dependencies_compiled or {}, "dependencies", "ecosystem", "dependency", repo)
    return _dedupe_linked_rows(rows)


def _mermaid_graph(repo, rows):
    lines = ["graph LR", f'    {_mermaid_id(repo)}["{repo}"]']
    seen = {repo}
    for row in rows:
        other = row["other_repo"]
        if other not in seen:
            lines.append(f'    {_mermaid_id(other)}["{other}"]')
            seen.add(other)
        label = row["name"]
        # Visual distinction by kind (architecture-diagrams spec): dashed for interface, solid
        # for dependency, thick/double for a linked (deduplicated) pair.
        arrow = {"interface": "-.->", "dependency": "-->", "linked": "==>"}[row["kind"]]
        if row["consumes"] and not row["produces"]:
            lines.append(f"    {_mermaid_id(other)} {arrow}|{label}| {_mermaid_id(repo)}")
        else:
            # produces (with or without also consuming), or owns-only: repo -> other.
            lines.append(f"    {_mermaid_id(repo)} {arrow}|{label}| {_mermaid_id(other)}")
    return "\n".join(lines)


_KIND_LABELS = {"interface": "interface", "dependency": "dependency", "linked": "interface + dependency"}


def _table(rows):
    lines = [
        "| Kind | Name | Type/Ecosystem | Direction | Other repo | Other repo's role |",
        "| --- | --- | --- | --- | --- | --- |",
    ]
    for row in rows:
        other = row["other_repo"]
        link = f"[{other}]({other}/architecture.md)"
        lines.append(
            f"| {_KIND_LABELS[row['kind']]} | `{row['name']}` | {row['type']} | {row['direction']} | "
            f"{link} | {row['other_role']} |"
        )
    return "\n".join(lines)


def render_org_diagram(interfaces_compiled, dependencies_compiled=None, diagram_format=None):
    """Deterministic org-diagram document (design D3/D4). `dependencies_compiled` defaults to
    empty (interface-only rendering, unchanged from before dependency-indexing existed).
    `diagram_format` tags each repo section's fenced graph block (default: DEFAULT_DIAGRAM_FORMAT)
    — callers are responsible for rejecting unsupported formats before calling this
    (config.require_supported_diagram_format); this function only renders Mermaid graph syntax,
    the only implemented renderer."""
    dependencies_compiled = dependencies_compiled or {}
    fmt = diagram_format or DEFAULT_DIAGRAM_FORMAT
    repos = sorted(
        {r for entries in interfaces_compiled.get("interfaces", {}).values() for entry in entries for r in repo_set(entry)}
        | {r for entries in dependencies_compiled.get("dependencies", {}).values() for entry in entries for r in repo_set(entry)}
    )
    lines = [_GENERATED_HEADER, "", "# Organization architecture", ""]
    any_section = False
    for repo in repos:
        rows = relationships_for_repo(interfaces_compiled, repo, dependencies_compiled)
        if not rows:
            continue
        any_section = True
        lines += [
            f"## {repo}",
            "",
            f"See this repo's own diagram: [{repo}/architecture.md]({repo}/architecture.md)",
            "",
            f"```{fmt}",
            _mermaid_graph(repo, rows),
            "```",
            "",
            _table(rows),
            "",
        ]
    if not any_section:
        lines += ["No cross-repo interface or dependency relationships yet.", ""]
    return "\n".join(lines)


def write_org_diagram(instance_root, diagram_format=None):
    """Read both compiled indices fresh from `instance_root` (missing files treated as empty —
    e.g. a repo with no dependency shard yet) and write the combined org diagram. Reading from
    disk rather than accepting in-memory compiled docs lets either merge path
    (`merge.merge_into_instance`, `dependency_merge.merge_into_instance`) trigger the same
    rebuild and always render the other index's current state too, never a stale one."""
    from .dependencies import KIND_COMPILED as DEP_KIND_COMPILED
    from .dependencies import empty_index as empty_dependency_index
    from .dependencies import load_index as load_dependency_index
    from .index import KIND_COMPILED as IFACE_KIND_COMPILED
    from .index import empty_index as empty_interface_index
    from .index import load_index as load_interface_index
    from .merge import COMPILED_BASENAME as IFACE_COMPILED_BASENAME
    from .merge import INTERFACES_DIR
    from .dependency_merge import COMPILED_BASENAME as DEP_COMPILED_BASENAME
    from .dependency_merge import DEPENDENCIES_DIR

    instance_root = Path(instance_root)
    interfaces_path = instance_root / INTERFACES_DIR / IFACE_COMPILED_BASENAME
    dependencies_path = instance_root / DEPENDENCIES_DIR / DEP_COMPILED_BASENAME
    interfaces_compiled = (
        load_interface_index(interfaces_path, kind=IFACE_KIND_COMPILED)
        if interfaces_path.is_file()
        else empty_interface_index(IFACE_KIND_COMPILED)
    )
    dependencies_compiled = (
        load_dependency_index(dependencies_path, kind=DEP_KIND_COMPILED)
        if dependencies_path.is_file()
        else empty_dependency_index(DEP_KIND_COMPILED)
    )
    path = instance_root / ORG_DIAGRAM_PATH
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        render_org_diagram(interfaces_compiled, dependencies_compiled, diagram_format), encoding="utf-8"
    )
    return path
